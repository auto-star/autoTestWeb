package com.cd.autoTest.model;

public class DegradeData {
	private int id;
	private int runCaseResultId;
	private String casePageName;
	private String itemName;
	private String pageValue;
	private int isCompare;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCasePageName() {
		return casePageName;
	}
	public void setCasePageName(String casePageName) {
		this.casePageName = casePageName;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getPageValue() {
		return pageValue;
	}
	public void setPageValue(String pageValue) {
		this.pageValue = pageValue;
	}
	public int getRunCaseResultId() {
		return runCaseResultId;
	}
	public void setRunCaseResultId(int runCaseResultId) {
		this.runCaseResultId = runCaseResultId;
	}
	public int getIsCompare() {
		return isCompare;
	}
	public void setIsCompare(int isCompare) {
		this.isCompare = isCompare;
	}
	
	
	
}
